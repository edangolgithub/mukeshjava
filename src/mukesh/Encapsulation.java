package mukesh;

public class Encapsulation
{
	
	public void setC(int c) {
		this.c = c;
	}
public int a;
protected int b;

public int getA() {
	return a;
}
public void setA(int a) {
	this.a = a;
}
public int getB() {
	return b;
}
public void setB(int b) {
	this.b = b;
}
public int getC() {
	return c;
}



private int c;
//getter setter
}
